#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	i;

	i = 1000000000;
	if (nb == 0)
	{
		write(1, "0", 1);
		return ;
	}
	if (nb == -2147483648)
	{
		write(1, "2", 1);
		nb = 147483648;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	while (i > nb)
		i = i / 10;
	while (i >= 1)
	{
		write(1, &"0123456789"[(nb / i) % 10], 1);
		i = i / 10;
	}
}
