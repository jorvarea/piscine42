int ft_atoi(char *str)
{
    int i;
    int sign;
    int is_num;

    is_num = 0;
    sign = 1;
    i = 0;
    while (str[i] != '\0')
    {
        if (str[i] == '-')
            sign *= -1;
        if (str[i] >= '0' && str[i] <= '9')
            is_num = 1;
        i++;
    }
}
